// Helper functions
export const getNewsById = (id) => {
  return newsData.find(news => news.id === parseInt(id));
};

export const getNewsBySlug = (slug) => {
  return newsData.find(news => news.slug === slug);
};

export const getNewsByCategory = (category) => {
  if (!category || category === 'all') return newsData;
  return newsData.filter(news => news.category.toLowerCase() === category.toLowerCase());
};

export const getLatestNews = (limit = 3) => {
  return newsData
    .sort((a, b) => new Date(b.date) - new Date(a.date))
    .slice(0, limit);
};

export const formatDate = (dateString) => {
  const months = ['Januari', 'Februari', 'Maret', 'April', 'Mei', 'Juni', 
                  'Juli', 'Agustus', 'September', 'Oktober', 'November', 'Desember'];
  const date = new Date(dateString);
  return `${date.getDate()} ${months[date.getMonth()]} ${date.getFullYear()}`;
};

export const getCategoryColor = (category) => {
  const colors = {
    'Pendaftaran': 'blue',
    'Prestasi': 'green',
    'Kegiatan': 'teal',
    'Program': 'purple',
    'Beasiswa': 'yellow',
    'Pengumuman': 'orange'
  };
  return colors[category] || 'teal';
};
